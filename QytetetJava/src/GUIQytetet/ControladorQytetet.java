    private void jJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jJugarActionPerformed
        this.jSalirCarcelPagando.setEnabled(false);
        this.jSalirCarcelDado.setEnabled(false);
        this.habilitarComenzarTurno();
        this.jJugar.setEnabled(false);
        
        if(modeloQytetet.getJugadorActual().getSaldo() > 0){
            boolean noTienePropietario = !(modeloQytetet.jugar());
            this.vistaQytetet.actualizar(modeloQytetet);
            if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == TipoCasilla.CALLE) {
                if (noTienePropietario)
                    this.jComprarPropiedad.setEnabled(true);

                this.jSiguienteJugador.setEnabled(true);
            }else if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == TipoCasilla.SORPRESA) {
                this.vistaQytetet.actualizar(modeloQytetet);
                this.vistaQytetet.mostrarCartaSorpresa(modeloQytetet);

                this.jSiguienteJugador.setEnabled(false);
                this.jGestionInmobiliaria.setEnabled(false);
                this.jAplicarSorpresa.setEnabled(true);
            }
            else
                this.jSiguienteJugador.setEnabled(true);
        }
        if(modeloQytetet.getJugadorActual().tengoPropiedades())
            this.jGestionInmobiliaria.setEnabled(true);
        
        this.vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jJugarActionPerformed

    private void jComprarPropiedadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComprarPropiedadActionPerformed
        boolean puedoComprarPropiedad = modeloQytetet.comprarTituloPropiedad();
        if(!puedoComprarPropiedad)
            JOptionPane.showMessageDialog(this, "¡No puedes comprar la calle!");
        else
            this.jGestionInmobiliaria.setEnabled(true);
        
        this.jComprarPropiedad.setEnabled(false);
        
        this.vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jComprarPropiedadActionPerformed

    private void jAplicarSorpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAplicarSorpresaActionPerformed
        boolean noTienePropietario = !(modeloQytetet.aplicarSorpresa());
        if(modeloQytetet.getJugadorActual().getSaldo() > 0){
            if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == TipoCasilla.CALLE)
                if (noTienePropietario)
                    this.jComprarPropiedad.setEnabled(true);
        }
        
        this.jAplicarSorpresa.setEnabled(false);
        this.jSiguienteJugador.setEnabled(true);
        if (!modeloQytetet.getJugadorActual().getEncarcelado() && 
                modeloQytetet.getJugadorActual().getSaldo() > 0 && 
                modeloQytetet.getJugadorActual().tengoPropiedades())
            this.jGestionInmobiliaria.setEnabled(true);
        
        this.vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jAplicarSorpresaActionPerformed
    
    private void jGestionInmobiliariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGestionInmobiliariaActionPerformed
        this.vistaQytetet.gestionInmobiliaria(" ");
        if (modeloQytetet.getJugadorActual().getSaldo() > 0 &&
                modeloQytetet.getJugadorActual().tengoPropiedades()) {
            SeleccionarOpcionInmobiliaria opcionInmobiliaria 
            = new SeleccionarOpcionInmobiliaria(this, rootPaneCheckingEnabled);
            opcionInmobiliaria.setVisible(true);

            int opcion = opcionInmobiliaria.obtenerOpcion();
            Calle opcionCasilla = null;
            ArrayList<Calle> misPropiedades = new ArrayList();

            if (opcion==1 || opcion==2 || opcion==3 || opcion==4)
                misPropiedades = modeloQytetet.propiedadesHipotecadasJugador(false);
            else if (opcion==5 )
                misPropiedades = modeloQytetet.propiedadesHipotecadasJugador(true);

            if(opcion != 0){
                String[] prop = new String[12];
                int i = 0;
                for (Calle c : misPropiedades) {
                    prop[i] = c.getTitulo().getNombre();
                    i++;
                }

                PanelPropiedades panel = new PanelPropiedades(this, rootPaneCheckingEnabled);
                panel.setModel(new javax.swing.DefaultComboBoxModel(prop));
                panel.setVisible(true);

                for(Calle c : misPropiedades)
                    if(panel.obtenerPropiedad().equals(c.getTitulo().getNombre()))
                        opcionCasilla = c;

            }
            if(opcionCasilla != null)
                switch (opcion) {
                    case 1:
                        boolean puedoEdificarCasa = modeloQytetet.edificarCasa(opcionCasilla);
                        if(puedoEdificarCasa)
                            vistaQytetet.gestionInmobiliaria("Has edificado una casa");
                        else
                            vistaQytetet.gestionInmobiliaria("¡No puedes edificar ninguna casa!");
                        break;
                    case 2: 
                        boolean puedoEdificarHotel = modeloQytetet.edificarHotel(opcionCasilla);
                        if(puedoEdificarHotel)
                            vistaQytetet.gestionInmobiliaria("Has edificado un hotel");
                        else
                            vistaQytetet.gestionInmobiliaria("¡No puedes edificar ningún hotel!");
                        break;
                    case 3: 
                        boolean puedoVenderPropiedad = modeloQytetet.venderPropiedad(opcionCasilla);
                        if(puedoVenderPropiedad)
                            vistaQytetet.gestionInmobiliaria("Has vendido la propiedad");
                        else
                            vistaQytetet.gestionInmobiliaria("¡No puedes vender la propiedad!");
                        break;
                    case 4:
                        boolean puedoHipotecarPropiedad = modeloQytetet.hipotecarPropiedad(opcionCasilla);
                        if(puedoHipotecarPropiedad)
                            vistaQytetet.gestionInmobiliaria("Has hipotecado la propiedad");
                        else
                            vistaQytetet.gestionInmobiliaria("¡No puedes hipotecar la propiedad!");
                        break;
                    case 5:
                        boolean puedoCancelarHipoteca = modeloQytetet.cancelarHipoteca(opcionCasilla);
                        if(puedoCancelarHipoteca)
                            vistaQytetet.gestionInmobiliaria("Has cancelado la hipoteca");
                        else
                            vistaQytetet.gestionInmobiliaria("¡No cancelar la hipoteca!");
                        break;
                    default : 
                        break;
                }
                
        }
        vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jGestionInmobiliariaActionPerformed


