    private void jSalirCarcelDadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalirCarcelDadoActionPerformed
        boolean resultado =
                modeloQytetet.intentarSalirCarcel(MetodoSalirCarcel.TIRANDODADO);
        this.jSalirCarcelDado.setEnabled(false);
        this.jSalirCarcelPagando.setEnabled(false);
        if(resultado){
            JOptionPane.showMessageDialog(this, "Sales de la cárcel");
            this.jJugar.setEnabled(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "NO sales de la carcel");
            this.jSiguienteJugador.setEnabled(true);
        }
        this.vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jSalirCarcelDadoActionPerformed

    private void jSiguienteJugadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSiguienteJugadorActionPerformed
        if(modeloQytetet.getJugadorActual().getSaldo() <= 0){
            this.jJugar.setEnabled(false);
            this.jSiguienteJugador.setEnabled(false);
            this.jGestionInmobiliaria.setEnabled(false);
            this.vistaQytetet.finJuego();
            JOptionPane.showMessageDialog(this, modeloQytetet.obtenerRanking());
            this.dispose();
        }
        
        modeloQytetet.siguienteJugador();
        this.habilitarComenzarTurno();        
        this.vistaQytetet.actualizar(modeloQytetet);
    }//GEN-LAST:event_jSiguienteJugadorActionPerformed

